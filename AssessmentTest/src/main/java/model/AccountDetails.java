package model;

public class AccountDetails {

    private String name;

    private String email;
    private String gender;

    private String password;

    private String dayOfDob;

    private String monthOfDob;

    private String yearOfDob;

    private String firstName;

    private String lastName;

    private String company;

    private String address;

    private String country;

    private String state;

    private String city;

    private String zipcode;

    private String mobileNumber;

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDayOfDob() {
        return dayOfDob;
    }

    public void setDayOfDob(String dayOfDob) {
        this.dayOfDob = dayOfDob;
    }

    public String getMonthOfDob() {
        return monthOfDob;
    }

    public void setMonthOfDob(String monthOfDob) {
        this.monthOfDob = monthOfDob;
    }

    public String getYearOfDob() {
        return yearOfDob;
    }

    public void setYearOfDob(String yearOfDob) {
        this.yearOfDob = yearOfDob;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getZipcode() {
        return zipcode;
    }

    public void setZipcode(String zipcode) {
        this.zipcode = zipcode;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
